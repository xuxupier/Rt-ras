set -euo pipefail

VERSION="v4.0.0"
APP_USER="fmrec"
APP_GROUP="fmrec"
BASE_DIR="/opt/fmrec"
APP_DIR="$BASE_DIR/app"
VENV_DIR="$BASE_DIR/venv"
DATA_DIR="/var/lib/fmrec"
RUN_DIR="/run/fmrec"

echo "=== FMREC installer $VERSION ==="

if [[ $EUID -ne 0 ]]; then
  echo "Please run as root: sudo bash $0"
  exit 1
fi

export DEBIAN_FRONTEND=noninteractive

step() { echo -e "\n[$1] $2"; }

step "1/11" "Installing apt dependencies..."
apt-get update -y
apt-get install -y --no-install-recommends \
  ca-certificates curl unzip \
  python3 python3-venv python3-pip \
  sqlite3 \
  rtl-sdr \
  ffmpeg sox \
  usbutils \
  systemd

step "2/11" "Creating user and directories..."
if ! id -u "$APP_USER" >/dev/null 2>&1; then
  useradd -r -m -s /usr/sbin/nologin "$APP_USER"
fi
mkdir -p "$APP_DIR" "$VENV_DIR" "$DATA_DIR" "$RUN_DIR"
mkdir -p "$DATA_DIR/recordings"
chown -R "$APP_USER:$APP_GROUP" "$BASE_DIR" "$DATA_DIR" "$RUN_DIR"
chmod 775 "$RUN_DIR" || true

step "3/11" "Udev rules for RTL-SDR (permissions)..."
# Prefer packaged rule if exists
if [[ -f /lib/udev/rules.d/rtl-sdr.rules ]]; then
  cp -f /lib/udev/rules.d/rtl-sdr.rules /etc/udev/rules.d/20-rtl-sdr.rules
else
  cat >/etc/udev/rules.d/20-rtl-sdr.rules <<'RULES'
SUBSYSTEM=="usb", ATTRS{idVendor}=="0bda", ATTRS{idProduct}=="2838", MODE:="0666"
RULES
fi
if getent group plugdev >/dev/null 2>&1; then
  usermod -aG plugdev "$APP_USER" || true
fi
udevadm control --reload-rules || true
udevadm trigger || true

step "4/11" "Blacklisting DVB driver (recommended)..."
cat >/etc/modprobe.d/rtl-sdr-blacklist.conf <<'BL'
blacklist dvb_usb_rtl28xxu
BL

step "5/11" "Deploying application bundle..."
rm -rf "$APP_DIR"
mkdir -p "$APP_DIR"
chown -R "$APP_USER:$APP_GROUP" "$APP_DIR"

