pip_retry() {
  local n=0
  until [[ $n -ge 5 ]]; do
    if "$VENV_DIR/bin/python" -m pip install -r "$APP_DIR/requirements.txt"; then
      return 0
    fi
    n=$((n+1))
    echo "pip failed, retry $n/5 ..."
    sleep $((n*3))
  done
  return 1
}
pip_retry >/dev/null

